package Relational;



public class Relational_demo {

	
	public String equal(int a, int b)
	{
		if ( a == b)
			return "yes";
		else 
			return "no";
		
	}
	

	public String equal(float a, float b)
	{
		if ( a == b)
			return "yes";
		else 
			return "no";
	}
	
	public String notEqual(int a, int b)
	{
		if ( a != b)
			return "yes";
		else 
			return "no";
	}
	

	public String notEqual(float a, float b)
	{
		if ( a != b)
			return "yes";
		else 
			return "no";
	}
	
	public String greater(int a, int b)
	{
		if ( a > b)
			return "yes";
		else 
			return "no";
		
	}
	

	public String greater(float a, float b)
	{
		if ( a > b)
			return "yes";
		else 
			return "no";
	}
	
	public String less(int a, int b)
	{
		if ( a < b)
			return "yes";
		else 
			return "no";
		
	}
	

	public String less(float a, float b)
	{
		if ( a < b)
			return "yes";
		else 
			return "no";
	}
	
	public String gtEqual(int a, int b)
	{
		if ( a >= b)
			return "yes";
		else 
			return "no";
		
	}
	

	public String gtEqual(float a, float b)
	{
		if ( a >= b)
			return "yes";
		else 
			return "no";
	}
	
	public String ltEqual(int a, int b)
	{
		if ( a <= b)
			return "yes";
		else 
			return "no"; 
		
	}
	

	public String ltEqual(float a, float b)
	{
		if ( a <= b)
			return "yes";
		else 
			return "no"; 
	}
	
	
}

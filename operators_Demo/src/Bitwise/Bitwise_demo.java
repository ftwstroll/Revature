package Bitwise;

public class Bitwise_demo {

	
	public int and(int a, int b)
	{
		return a & b; 
	
	}
	

	public int or(int a, int b)
	{
		return a | b;
	}
	
	public int xor(int a, int b)
	{
		return a ^ b;
	}
	

	public int compliment(int a)
	{
		return ~a;
	}
	
	public int leftShift(int a, int b)
	{
		return a<<b; 
		
	}
	

	public int rightShift(int a, int b)
	{
		return a>>b;
	}
	
	
	
	
}
